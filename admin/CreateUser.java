    private void createMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createMouseClicked
        // TODO add your handling code here:
        try{
            Connection con = DriverManager.getConnection("jdbc:mariadb://localhost:3306/baseis", "root", "");
            
            String check = "SELECT * FROM user WHERE username=? AND password=?";
            PreparedStatement pst = con.prepareStatement(check);
            pst.setString(1, CreateUsername.getText());
            pst.setString(2, CreatePassword.getText());
            
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "The user already exists");
            }
            else {
               
                 String password1= CreatePassword.getText();
                 String password2= ConfirmPassword.getText();
                 if(password1.equals(password2)){
                 JOptionPane.showMessageDialog(null, "The two passwords match");
                 
                 String timeStamp = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(Calendar.getInstance().getTime());
                 String isadmin ="No";
                 String store = "INSERT INTO user(username,password,user_name,user_surname,reg_date,email,is_admin) VALUES (?,?,?,?,?,?,?)";
                  
                 PreparedStatement cre = con.prepareStatement(store);
                 cre.setString(1, CreateUsername.getText());
                 cre.setString(2, CreatePassword.getText());
                 cre.setString(3, Name.getText());
                 cre.setString(4, Surname.getText());
                 cre.setString(5, timeStamp);
                 cre.setString(6, Email.getText());
                 cre.setString(7, isadmin);
            
                 ResultSet fd = cre.executeQuery();
                 JOptionPane.showMessageDialog(null, "User created");
               
                 
                 java.awt.EventQueue.invokeLater(new Runnable() {
                                     public void run() {
                                        new Choose().setVisible(true);
                                     }
                                    });
                
                 }
                 else{
                  JOptionPane.showMessageDialog(null, "The two passwords does not match");
                 }
                    
            }
            
            
    }//GEN-LAST:event_createMouseClicked
        catch(SQLException e) {
          JOptionPane.showMessageDialog(null, e);
        }
    }      
